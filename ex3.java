package example1;

public class ex3 {

	public static void main(String[] args) {
		
		int i = 1;
		
		while(i <= 5)
		{
			System.out.println(i);
			i++;
		}
		
		System.out.println("---------");
		
		for(i = 1; i <= 5; i ++)
		{
			System.out.println(i);
		}
		
		System.out.println("---------");
		
		// 나는 for문이 좋아..
		
		
		

	}

}
